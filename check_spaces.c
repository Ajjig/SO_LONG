#include "so_long.h"

void	check_spaces(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '\n')
			if (str[i + 1] == '\n' && str[i + 2] != 0)
				ft_exit(NO_RECTANGLE);
		i++;
	}
}
